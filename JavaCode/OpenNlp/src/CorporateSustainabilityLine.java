
public class CorporateSustainabilityLine {
	public String name;
	public float ecoValue;
	public float envValue;
	public float socValue;
	public CorporateSustainabilityLine(String name, float ecoValue, float envValue, float socValue) {
		super();
		this.name = name;
		this.ecoValue = ecoValue;
		this.envValue = envValue;
		this.socValue = socValue;
	}
	
}
